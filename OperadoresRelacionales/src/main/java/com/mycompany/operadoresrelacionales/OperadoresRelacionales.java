package com.mycompany.operadoresrelacionales;

public class OperadoresRelacionales {

    public static void main(String[] args) {
        int valor = 500, valor2 = 300;
        boolean resultado;
        
        resultado = valor < valor2;
        System.out.println(resultado);
        
        resultado = valor > valor2;
        System.out.println(resultado);
        
    }
}
